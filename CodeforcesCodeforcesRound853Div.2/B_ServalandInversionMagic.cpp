bool is_pali(string s) {
  int n = (int) s.size();
  for (int i = 0; i < n / 2; i++) {
    if (s[i] != s[n - 1 - i]) return false;
  }
  return true;
}

int main() {
  ios::sync_with_stdio(0); cin.tie(0);
  int tt = 1;
  cin >> tt;
  while (tt--) {
    int n;
    cin >> n;
    string s;
    cin >> s;
    vector<int> v;
    for (int i = 0; i < n - 1 - i; i++) {
      if (s[i] != s[n - 1 - i]) v.push_back(i);
    }
    if (v.empty() || v.back() - v[0] + 1 == v.size()) {
      cout << "Yes" << '\n';
    } else {
      cout << "No" << '\n';
    }
  }
}
