// n: 50
// time(s): 2
// order: O(1), O(logn), O(n), O(nlogn)
// ================= Solution ================= //
int main() {
    ios::sync_with_stdio(0); cin.tie(0);
    int tt = 1;
    cin >> tt;
    while (tt--) {
        int n;
        cin >> n;
        vector<pair<int, int>> v(n);
        for (int i = 0; i < n; i++) {
            int a, b;
            cin >> a >> b;
            v[i] = {a, b};
        }
        int cnt = 0;
        for (int i = 0; i < n; i++) {
            if (v[i].second < v[i].first) {
                cnt++;
            }
        }
        cout << cnt << '\n';
    }
}
